            <p><b>Accès direct à la liste des espèces</b></p>

            <p>Le menu principal permet d’accéder directement à la liste des espèces (indépendamment du système d’identification) selon différents modes de classification. Cliquez sur « Liste des espèces ».</p> 
            <p> Vous obtenez deux listes :
                  <ol>
                      <li>  L’une triée par famille puis par ordre alphabétique des noms scientifiques au sein des familles</li>
                      <li>  L’une triée par ordre alphabétique des noms scientifiques</li>
 
                  </ol>
            </p>

            
            <p><b>Limites de ce type d’outil</b></p>

            <p>Cet outil utilise des caractères d’identification correspondant à différents éléments végétatifs ou sexuels des plantes. Ces caractères sont facilement accessibles à partir d’échantillons de la majorité des adventices rencontrées dans les cultures de riz de la zone d’étude. Ils sont reconnus pour être relativement stables mais peuvent faire l’objet d’un certain polymorphisme en fonction de l’environnement ou du stade de développement de l’individu concerné. C’est pourquoi toute identification réalisée à l’aide de ce procédé doit être considérée avec précaution. Il est donc recommandé de confirmer cette identification par comparaison de l’échantillon avec un spécimen d’herbier de référence.</p>

          
