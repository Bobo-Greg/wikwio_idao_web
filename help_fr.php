            <p><b><h4>Accès aux résultats d’identification</h4></b></p>
            <p><b>La fenêtre des résultats</b></p>  

            <p>Pour visualiser les résultats de l’identification, cliquez sur le bouton « Résultats » de la barre d’outils. Une nouvelle fenêtre s’ouvre.</p>
                                <div align="center"><img src="img/results.jpg"></div>

            <p>Sélectionnez l’espèce dont vous voulez consulter la fiche descriptive</p>
            <p>
           La fiche descriptive de l’espèce est également accessible directement depuis la page d’accueil du portail Wikwio. Elle comprend les informations suivantes</p>
            <ol>
                  <li>  Le code OEPP (Organisation Européenne de Protection des Plantes) standard de la plante</li>
                  <li>  Le nom scientifique correct de l’espèce, ainsi que sa famille</li>
                  <li>  Les noms scientifiques synonymes</li>
                  <li>  Les noms communs dans différents langues</li>
                  <li>  La description synthétique, est un résumé simple mettant en évidence les principaux caractères de cette espèce.</li>
                  <li>  La description botanique décrit la plante et tous ses organes, très précisément depuis la plantule jusqu’à la plante adulte</li>
                  <li>  Un paragraphe Remarque existe parfois pour mentionner des confusions possibles avec d’autres espèces proches (décrites ou non décrites dans AdventOI) et avec lesquelles il pourrait y avoir confusion.</li>
                  <li>  La biologie de l’espèce.</li>
                  <li>  L’écologie de l’espèce</li>
                  <li>  La région d’origine de l’espèce et sa répartition mondiale</li>
                  <li>  L’agressivité de l’espèce d’un point de vue général</li>
                  <li>  L’agressivité de l’espèce dans les différents pays concernés par le projet (quand cette information est disponible)</li>
                  <li>  La lutte contre cette espèce d’un point de vue général avec des liens hypertextes vers les documents de recommandation de désherbage du riz disponibles sur la plateforme collaborative du projet.</li>
                  <li>  La lutte mise en œuvre dans les différents pays du projet (quand cette information est disponible)</li>
                  <li>  Les usages de l’espèce.</li>
                  <li>  La bibliographie utilisée</li>
                  <li>  Les liens Web accessibles pour cette espèce</li>
                  <li>  Une carte de répartition des photos</li>
                  <li>  Une série de photos de l’espèce à différents stades de développement, ainsi que différents détails. Chacune de ces photos peut être agrandie, il suffit de cliquer dessus. Chaque photo est accompagnée d’une légende et d’un copyright.</li>

            </ol>
