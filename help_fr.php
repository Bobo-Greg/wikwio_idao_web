            <p><b><h4>Interface de l’utilisateur</h4></b></p>
            <p>Interface principale</p>
            <p>Lorsque vous lancez le logiciel Wikwio-Idao une nouvelle fenêtre s’ouvre</p>
            <div align="center"><img src="img/home.jpg"></div>
            <p>Cette fenêtre comprend :</p>
           <ol>
            <li> Une barre d’outils</li>
            <li> Un portrait robot, qui correspond à l’interface graphique d’identification</li>
            <li> Une barre de statut dans laquelle est affiché le nombre d’espèces de la base de données</li>
            </ol>

            <p>La base de données de Wikwio-Idao comprend 345 espèces au démarrage du processus d’identification. Ce nombre est affiché dans le coin en bas à droite de la fenêtre. Pour identifier une de ces espèces, l’utilisateur doit renseigner le logiciel avec les caractères botaniques présents sur l’échantillon et qui lui paraissent les plus pertinents pour distinguer cette espèce des autres espèces.</p>

            <p>Examinez l’échantillon botanique pour trouver ce caractère. Pour accéder aux différentes modalités de ce caractère, déplacez la souris sur le portrait robot ou sur les boutons de droite jusqu’à arriver sur la partie du dessin correspondant au caractère choisi. Lors du déplacement de la souris sur une zone sensible, une boite d’information précise le caractère concerné. </p>
            <p><i>A noter que seules les parties colorées du portrait robot sont interactives. Les parties grisées demeurent inactives</i></p>
            <p><b><h4>Le procédé d’identification</h4></b></p> 
            <p>Bien qu’il soit possible de commencer par n’importe quel caractère présent sur le portrait robot, il existe une méthode particulièrement appropriée pour choisir les caractères les plus pertinents.</p>
            <p> Examinez les caractères dans l’ordre suivant :</p>
            <ol>
            <li>o Le port </li>
            <li>o Le type de feuille pour distinguer rapidement les espèces de type graminéennes des espèces à feuilles larges</li>
            <li>o Le type d’inflorescence, de fleur ou de fruit s’ils sont présents </li>
            <li>o La présence de latex dans la tige</li>
            </ol>
            <p>?  Choisissez un petit nombre de caractères pour lesquels votre information est précise plutôt qu’un grand nombre de caractères imprécis. Rappelez-vous que la qualité de l’identification dépend de la qualité de votre observation de l’échantillon.</p>

            <p><b>Exemple</b> </p>

            <p> Sélectionnez en premier le port de la plante en cliquant que le cadre localisé sur la tige ou sur le bouton en haut à droite de la barre d’outils. Une nouvelle fenêtre s’ouvre présentant les différents types de port.</p>

            <div align="center"><img src="img/port.jpg"></div>

            <p>Sélectionnez le port correspondant à votre plante en cliquant sur le dessin correspondant. </p>
            <p>Vous revenez à la fenêtre du portrait robot avec.</p>

             <div align="center"><img src="img/redrawdef.jpg"></div>

            <ol>
                  <li> Le type de port choisi s’affiche sur le portrait robot</li>
                  <li> Le nombre d’espèces possédant cette modalité de caractère (ce type de port) est affiché en bas à droite de la fenêtre.</li>
            </ol>

            <p>Durant le processus d’identification il est important de tenir compte des valeurs qui apparaissent dans ce cadre. Elles indiquent :</p>

            <ol>
                <li> Le nombre d’espèce possédant le caractère ou la combinaison de caractères renseignés</li>
                <li> Le pourcentage de similarité maximum entre les espèces et le portrait robot réalisé. Lorsque ce pourcentage est inférieur à 100 %, cela signifie que la combinaison des caractères renseignés ne correspond pas exactement à une espèce de la base de données. </li>
            </ol>
            

            <p>Il est possible, à tout moment, d’accéder à la liste des résultats en cliquant sur le bouton « Résultats », quel que soit le nombre d’espèce mentionné et leur pourcentage de similarité. Cependant il est préférable de poursuivre le processus d’identification tant que le nombre d’espèces mentionné reste supérieur à 10.</p>

            <p>Poursuite de l’identification</p>
            
            <p>Vous pouvez poursuivre l’identification en renseignant le caractère portant sur le type de feuille. Cliquez sur une feuille du portrait robot. Une nouvelle fenêtre s’ouvre, montrant les principaux types de feuilles. Sélectionnez la bonne modalité.</p>
            
             <div align="center"><img src="img/leaf.jpg"></div>


            <p>Le portrait robot est mis à jour montrant maintenant le port et le type de feuille de la plante en cours d’identification.</p>

             <div align="center"><img src="img/habitatleaf.jpg"></div>


            <p>Cliquez maintenant sur l’inflorescence du portrait robot. Une nouvelle fenêtre s’ouvre montrant différents types d’inflorescences. Sélectionnez celle qui correspond à votre échantillon.</p>

                                                <div align="center"><img src="img/infloroscense.jpg"></div>

            <p>Le portrait robot est mis à jour ainsi que le compteur des espèces correspondant à la combinaison des caractères renseignés et leur pourcentage maximum de similarité.</p>
                                                            <div align="center"><img src="img/infloupdate.jpg"></div>


            <p>Tout au long du processus d’identification et de construction du portrait robot, surveillez le nombre d’espèces sélectionnés et leur pourcentage de similitude. </p>
            <p>Lorsque ce compteur indique 1 espèce à 100 %, le système d’identification est arrivé à son terme. Cliquez sur le bouton « Résultats » de la barre d’outils. Vous obtenez la liste des espèces triées par ordre de pourcentage de similarité avec la combinaison des caractères renseignés.</p>

            <p>Il n’est pas obligatoire d’atteindre 1 espèce à 100 % pour visualiser les résultats. Lorsqu’il reste peu d’espèces (< 10) il peut être plus rapide et efficace de comparer les fiches descriptives des espèces à partir de la liste des résultats.</p>

            <p><b><h4>Le bouton « Recherche »</h4></b></p>
            <p>Si vous n’arrivez pas à identifier votre échantillon de plante ou si vous ne savez pas quel caractère choisir, vous pouvez demander au logiciel de choisir le caractère le plus pertinent. Pour cela cliquez sur le bouton « Recherche » de la barre d’outils.</p>
            <p>En fonction du groupe d’espèces déjà sélectionné par les premiers caractères renseignés, le logiciel va vous proposer le caractère le plus pertinent. Une nouvelle fenêtre s’ouvre, proposant les différentes modalités de ce caractère.</p>
            <p>Si vous pouvez répondre, sélectionnez la modalité correspondant à l’échantillon.</p>
            <p>Si vous ne pouvez pas répondre (indécision, caractère manquant que l’échantillon) cliquez sur le bouton « Passe » et le logiciel vous proposera un autre caractère à renseigner.</p>

            <p><b><h4>Remarques importantes</h4></b></p>
            <ol>
            <li> Pour modifier l’état d’un caractère précédemment renseigné, cliquez sur ce caractère, sélectionné la nouvelle modalité et validez. Le portrait robot sera mis à jour sans modifier les autres caractères renseignés. Les calculs sur les espèces seront également mis à jour.</li>
            <li> Vous pouvez également supprimer un caractère déjà renseigné. Cliquez sur le caractère, cliquez sur la modalité cochée de bleu, la coche bleue disparait, validez. Le nouveau portrait robot s’affiche sans le caractère.</li>
            <li> Cet outil permet l’identification des plantes à partir de caractères que vous choisissez ou que le logiciel choisit. Il n’est pas nécessaire de renseigner tous les caractères disponibles. La combinaison de quelques caractères pertinents et généralement suffisante pour identifier une plante rapidement.</li>
            <li> Certains caractères particuliers à quelques espèces permettent d’accéder très rapidement à un choix réduit d’espèce (ex : latex jaune).</li>
            <li> Certains caractères ne sont pas directement accessibles depuis le portrait robot initial. Ils dépendent d’un caractère qui doit être renseigné préalablement. C’est notamment le cas du caractère « type de pilosité de la face supérieure » qui n’est accessible que lorsque le caractère « pilosité de la face supérieure du limbe » est renseigné.</li>
            <li> A n’importe quel stade du processus d’identification il est possible d’obtenir l’état des résultats en cliquant sur le bouton « Résultats ».</li>
            </ol>

